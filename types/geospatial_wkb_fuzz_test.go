package types

import "testing"

func FuzzWkbToGeoJSON(f *testing.F) {
	// empty and too-short inputs
	f.Add([]byte{})
	f.Add([]byte{0x01, 0x01, 0x00})

	// LE point at (1,2) — copied from existing test fixture
	f.Add([]byte{
		0x01,
		0x01, 0x00, 0x00, 0x00,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xF0, 0x3F, // x=1.0
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x40, // y=2.0
	})
	// BE point at (0,0)
	f.Add([]byte{
		0x00,
		0x00, 0x00, 0x00, 0x01,
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // x=0.0
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // y=0.0
	})
	// LE linestring with 2 points: (0,0)→(1,1)
	f.Add([]byte{
		0x01,
		0x02, 0x00, 0x00, 0x00, // WKBLineString
		0x02, 0x00, 0x00, 0x00, // numPoints=2
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // x=0.0
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, // y=0.0
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xF0, 0x3F, // x=1.0
		0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xF0, 0x3F, // y=1.0
	})
	// unknown geometry type
	f.Add([]byte{0x01, 0xFF, 0xFF, 0xFF, 0xFF})

	f.Fuzz(func(t *testing.T, b []byte) {
		wkbToGeoJSON(b, 6)
	})
}
